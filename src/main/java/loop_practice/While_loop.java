package loop_practice;

public class While_loop {

	public static void main(String[] args) {
		int j=20;
		while(j>0) {
			System.out.println(j + "");
			j--;
			
		}
		int numbers = 2;
		while(numbers<=20) {
			System.out.println(numbers*2);
			numbers++;
			
			
		}
		int k = 1;
		while(k<=20) {
			System.out.println(k*2 + "");
			k = k+1;
		}
		
		int n=100;

		while(n>=1) {
		System.out.println(n + "");
		n= n-5;
		}


		}

		}





